class print_first10number {
    public static void main(String[] args) {
        print_first10number obj = new print_first10number();
        obj.printOneToTen(10);
        
    }
    public int printOneToTen(int n) {
        for(int i =1;i<=n;i++){
            System.out.print(i+"  ");
        }
    return 0;
    }
}



